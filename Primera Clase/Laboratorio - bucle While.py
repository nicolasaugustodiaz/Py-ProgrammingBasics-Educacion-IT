# Ejercicio: Utilizando un bucle “while”, elaborar un código
# que imprima en pantalla cada uno de los
# elementos de una lista y simultáneamente los
# elimine, hasta quedar vacía.

lista = ["a", "b", "c", "d", "e"]

while lista:
    print(lista)
    del lista[-1];
